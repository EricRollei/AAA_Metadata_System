def hex_to_rgb(hex_color: str) -> tuple:
    """Convert hex color to RGB tuple"""
    if not hex_color or hex_color == "":
        return None
    
    hex_color = hex_color.lstrip('#')
    return tuple(int(hex_color[i:i+2], 16) for i in (0, 2, 4))

def get_rgb_culture_mappings():
    """
    Process color culture data into a format usable by the analyzer
    
    Returns:
        Dict mapping RGB tuples to culture/concept information
    """
    # Dictionary to store RGB -> Culture -> Concepts mapping
    rgb_to_culture_concepts = {}
    
    # Process each concept
    for item in COLOR_CULTURE_DATA:
        concept = item["concept"]
        
        # Process each culture
        for culture in ["western_american", "japanese", "hindu", "native_american", 
                      "chinese", "asian", "eastern_european", "arab", 
                      "african", "south_american"]:
            
            # Get the hex color and convert to RGB
            hex_color = item.get(culture, "")
            if hex_color:
                rgb = hex_to_rgb(hex_color)
                
                if rgb:
                    # Initialize if needed
                    if rgb not in rgb_to_culture_concepts:
                        rgb_to_culture_concepts[rgb] = {}
                    
                    # Normalize culture name
                    normalized_culture = culture.replace("_", " ")
                    
                    # Add concept to this culture
                    if normalized_culture not in rgb_to_culture_concepts[rgb]:
                        rgb_to_culture_concepts[rgb][normalized_culture] = []
                    
                    rgb_to_culture_concepts[rgb][normalized_culture].append(concept.lower())
    
    return rgb_to_culture_concepts

# Pre-processed mapping for direct import
RGB_CULTURE_CONCEPTS = get_rgb_culture_mappings()